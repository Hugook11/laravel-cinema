                        <p id="texte">Le cinéma a ouvert ses portes le 02 juin 1993 avec Ma saison
préférée d’André Téchiné, il est né d’une volonté municipale
pour un cinéma de proximité ouvert à tous.
Durant l’été 1993, l’association Ramonville ciné est créée,
l’exploitation, la programmation et la gestion lui sont
confiées. <br><br>
En septembre 1993, le cinéma rouvre sur la base de 10
séances par semaine et la salle prend le nom de L’Autan.
Au fil des années, l’association s’est attachée à promouvoir
les œuvres cinématographiques de qualité, à défendre un
cinéma de proximité et de convivialité, à favoriser les liens.
Classée Art & Essai, la salle bénéficie aussi des labels Jeune
Public, Recherche et Découverte et Patrimoine et Répertoire.
Impliquée dans la vie locale, elle organise des ciné-débats,
ciné-rencontres (en partenariat avec des associations
locales), des Thés au ciné (un après- midi chaque mois) cinép’tits déj, ciné-goûters pour le Jeune Public, des séances
scolaires. <br><br>
Le cinéma L’Autan accueille également des séances en
partenariat avec des festivals toulousains.
La salle peut accueillir 201 spectateurs et est accessible aux
personnes à mobilité réduite.
Elle bénéficie d’un équipement permettant d’assurer des
séances en Version Sourds et Malentendants (VSM), ainsi
qu’en Audio-Description (AD) pour les spectateurs
malvoyants. 
La cabine de projection est équipée du numérique et du 35
mm.<br><br>
L’équipe est composée de plusieurs salariés : sous la
responsabilité de Jérôme,
Sophie est agent de caisse, Marjorie (ou Thierry) agents
polyvalents de cinéma, de même qu’Henry, mis à disposition
par la municipalité.
Une précieuse équipe de bénévoles de l’association apporte
son aide au quotidien ou lors de ciné-rencontres/débats, de
moments de convivialité.
L’association Ramonville ciné et ses membres tiennent une
place importante au sein du cinéma L’Autan.
Présidé par Geneviève Birman, le conseil d’administration est
composé de 12 membres élus et 4 membres de droit.</p>
